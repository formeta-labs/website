export function getCompanySuggestions(query: string): string[] {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  return commonCompanies
    .filter(company => company.toLowerCase().includes(lowerQuery))
    .slice(0, 8);
}

export function getPositionSuggestions(query: string): string[] {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  return commonPositions
    .filter(position => position.toLowerCase().includes(lowerQuery))
    .slice(0, 8);
}

export function getEmailSuggestions(query: string): string[] {
  if (!query || !query.includes('@')) return [];
  
  const [localPart, domainPart] = query.split('@');
  if (!domainPart) return [];
  
  const lowerDomain = domainPart.toLowerCase();
  return emailDomains
    .filter(domain => domain.toLowerCase().startsWith(lowerDomain))
    .map(domain => `${localPart}@${domain}`)
    .slice(0, 5);
}
